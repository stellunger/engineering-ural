					поэтому применение стеклопластика в аппаратах мокрой
					газоочистки является особенно эффективным.</p>

					<h2>Скруббер</h2>

					<p><strong>Скруббер</strong> - аппарат, применяемый в
					различных химико-технологических процессах для глубокой
					очистки газов от частиц пыли, дыма, аэрозолей и твердых
					примесей.</p>

					<p><strong>Основные функции скрубберов</strong></p>

					<ul>
						<li>Глубокая очистка газов от посторонних примесей.</li>
						<li>Очистка от мелкой и среднедисперсной пыли.</li>
						<li>Понижение температуры дымовых газов.</li>
						<li>Испарительное увлажнение воздуха.</li>
						<li>Утилизация тепла уходящих газов.</li>
					</ul>

					<p><strong>Принцип работы скруббера</strong></p>

					<p>	Парогазовоздушная среда от источника загрязнения подаётся
					в технологический блок скруббера. В нём газовая среда 
					совместно с жидкой поступает в камеру инжектора через
					закручиватели, расположенные в нижней его части.</p>

					<p>При этом	в объёме образующейся в камере динамической
					пены происходит	интенсивный массообмен между фазами.
					Соединения из газовой фазы переходят в жидкую технологическую
					среду. Очищенная газовая среда направляется в атмосферу, а
					жидкая среда через сливной патрубок поступает в
					приёмно-разделительную емкость скруббера.</p>

					<h2>Циклон</h2>

					<p><strong>Циклон</strong> - аппарат газоочистки, широко
					применяемый для	отделения пыли от газов и воздуха в
					промышленности.</p>

					<p><strong>Принцип работы циклона</strong></p>

					<p>Газ поступает в верхнюю часть корпуса через входной
					патрубок. Пылеулавливание и очистка газов происходит под
					действием центробежной силы, возникающей при движении газа
					между корпусом и выхлопным патрубком.</p>

					<p>Уловленная пыль попадает в бункер, а очищенный газ
					выбрасывается через	выхлопной патрубок в атмосферу или
					поступает на дополнительную	газоочистку.</p>

					<h2>Каплеуловитель</h2>

					<p><strong>Каплеуловитель</strong> - аппарат для удаления
					капель жидкости с осевшими на них частицами пыли из основного
					технологического оборудования.</p>

					<p><strong>Принцип работы каплеуловителя</strong></p>
					
					<p>Ввод газа в каплеуловитель осуществляется тангенциально.
					При помощи завихрителя создается вращение газового потока,
					вследствие чего смоченные и укрупненные частицы пыли
					отбрасываются на стенки и непрерывно удаляются из каплеуловителя
					в виде шлама.</p>

					<p>Каплеуловители устанавливаются в применяют в системах
					дымо- и газоудаления для сбора агрессивного конденсата и
					меньшения его выброса в окружающую среду.</p>					
					
