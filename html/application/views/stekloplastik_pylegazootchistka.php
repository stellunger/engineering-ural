					время для многих предприятий задачи пылегазоочистки являются
					актуальными.</p>

					<p>Процессы пылегазоочистки подразделяются <strong>на
					мокрые и сухие.</strong></p>

					<p><strong>Принципиальной особенностью мокрой пылегазоочистки</strong>
					является применение жидкости для связывания и удаления
					аэрозольных частиц твердых материалов. Процесс мокрого
					пылеулавливания протекает в две стадии: на первой происходит
					движение и связывание пыли и жидкости, на второй пылеводяные
					частицы выводятся из потока.</p>

					<p><strong>Оборудование мокрой пылегазоочистки</strong> позволяет
					производить	очистку выбросов от вредных химических веществ:</p>

					<ul>
						<li>окиси азота;</li>
						<li>сернистого газа;</li>
						<li>паров кислот (азотной, соляной, фтористоводородной);</li>
						<li>аммиака;</li>
						<li>хлора газообразного;</li>
						<li>и других вредных агрессивных веществ, улавливаемых
						по механизму хемосорбции (за счет быстрой необратимой
						реакции между газовой и жидкой фазами).</li>
					</ul>

					<p><strong>Преимущества аппаратов мокрой пылегазоочистки:</strong></p>

					<ul>
						<li>простота конструкции и обслуживания;</li>
						<li>возможность очистки взрывоопасных газов;</li>
						<li>независимость от исходной температуры газов и
						возможность охлаждения таких газов при пылсочистке;</li>
						<li>возможность одновременной очистки газов от пыли и
						ряда химических газовых элементов.</li>
					</ul>

					<p>К основным недостаткам мокрого процесса пылегазоочистки
